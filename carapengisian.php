<h2>Cara Pengisian Form Pembobotan dengan AHP</h2>

Perhatikan form pembobotan yang berisi dengan:

<ul>
<li>Konteks Pembobotan yang tercantum di atasnya</li>
<li>Judul-judul pada kolom dari formulir pembobotan, yaitu:</li>
	<ol>
	<li>Kolom faktor/elemen yang akan dibandingkan (judul kolom di bagian paling kiri dan kanan)</li>
	<li>Kolom penilaian dari 1-9</li>
	</ol>
<li>Pembobotan dilakukan dengan membandingkan faktor/elemen di sebelah kiri dengan di sebelah kanan</li>
<li>Pembobotan dilakukan dengan memilih salah satu dari angka/nilai yang telah disediakan di setiap barisnya.</li>
<li>Nilai harus sesuai dengan arti penilaian sebagai berikut</li>
